    private void btnOKEventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKEventoActionPerformed
        iEvento = jcbEvento.getSelectedIndex();
        if (iEvento != -1) {
            Evento e = new Evento();
            decidirSobreArtigoController.setM_evento(e);
            listaMecanimosDecisao = decidirSobreArtigoController.getListaMecanismosDecisao();

            if (listaMecanimosDecisao.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Não existe mecanismo de decisao", "Decisão Artigo",
                        JOptionPane.ERROR_MESSAGE);
                dispose();
            } else {
                for (MecanismoDecisao MecanismoDecisao : listaMecanimosDecisao) {
                    jcMecanismos.addItem(MecanismoDecisao.getNome());
                }
               
                jPanelMecanismo.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Tem que seleccionar um evento primeiro", "Decisão", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnOKEventoActionPerformed

    private void btnOKMecanismoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKMecanismoActionPerformed
        iMecanismo = jcMecanismos.getSelectedIndex();
        listaDecisoes = new ArrayList<>();
        String decisao = "";
        if (iMecanismo != -1) {
            decidirSobreArtigoController.setMecanismoDecisao(iMecanismo);
            JOptionPane.showMessageDialog(this, "Decisão definida com Sucesso", "Decisão", JOptionPane.OK_OPTION);
            listaDecisoes = decidirSobreArtigoController.getListaDecisoes();
            if (listaDecisoes != null) {
                for (int i = 0; i < listaDecisoes.size(); i++) {
                    decisao = decisao + decidirSobreArtigoController.getProcessoDecisao().getM_decisao()+ "/n"+
                            decidirSobreArtigoController.getProcessoDecisao().getM_listaDistribuicao().toString();
                    }
                    decisao = decisao + "\n";
                }
                JOptionPane.showMessageDialog(this, decisao, "Decisao!", JOptionPane.OK_OPTION);
            } else {
                JOptionPane.showMessageDialog(this, "Nao existem dados suficientes para efectuar a decisao!", "Erro!", JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        
    
    }//GEN-LAST:event_btnOKMecanismoActionPerformed

