    public void run() {
        String organizador = JOptionPane.showInputDialog(this,
                "Insira o ID de organizador", "Login", JOptionPane.INFORMATION_MESSAGE);
        if (organizador != null) {

            listaEventos = decidirSobreArtigoController.ListarEventosOrganizador(organizador);

            if (listaEventos.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Utilizador não associado a nenhum evento", "Distribuir Revisões",
                        JOptionPane.ERROR_MESSAGE);
                dispose();
            } else {
                for (Evento evento : listaEventos) {
                    jcbEvento.addItem(evento.getM_strTitulo());
                }
                pack();
                setResizable(false);
                setVisible(true);
            }
        } else {
            JOptionPane.showInputDialog(this,
                    "O ID introduzido não é válido", "Login", JOptionPane.ERROR_MESSAGE);
        }

    }
