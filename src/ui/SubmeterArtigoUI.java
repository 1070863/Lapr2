    private void btnSubmeterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmeterActionPerformed
        // TODO add your handling code here:
        iEvento = cmbEvento.getSelectedIndex();
        if (iEvento != -1) {
            //Adiciona os dados da submiss√£o
            if (!(txtTitulo.getText().isEmpty() || txtResumo.getText().isEmpty())) {

                submeterArtigoController.iniciarSubmissao();
                submeterArtigoController.selectEvento(empresa.getM_registoEventos().
                        getEvento((String) cmbEvento.getSelectedItem()));

                submeterArtigoController.setDados(txtTitulo.getText(), txtResumo.getText());

                while (JOptionPane.showConfirmDialog(this, "Adicionar autor?",
                        "Adicionar autores",
                        JOptionPane.YES_NO_OPTION) == 0) {

                    String nomeAutor = JOptionPane.showInputDialog(this, "Nome do autor:",
                            "Inserir autores", JOptionPane.OK_CANCEL_OPTION);
                    Utilizador utilizador = submeterArtigoController.obterAutor(nomeAutor);
                    String afiliacao = "";
                    if (utilizador != null) {
                        afiliacao = JOptionPane.showInputDialog(this, "Afilia√ß√£o:",
                                "Inserir afilia√ß√£o", JOptionPane.OK_CANCEL_OPTION);

                        submeterArtigoController.addAutor(submeterArtigoController.
                                novoAutor(utilizador.getM_strNome(), afiliacao,
                                        utilizador.getM_strEmail()));
                        submeterArtigoController.setTipoArtigo((String) cmbTipo.
                                getSelectedItem());

                    } else {
                        JOptionPane.showMessageDialog(this, "O autor introduzido n√£o faz parte da lista de utilizadores!",
                                "Submeter artigo", JOptionPane.INFORMATION_MESSAGE);
                    }

                }
                jPan1.setVisible(false);
                jPanel2.setVisible(true);
                for (Autor autorCorrespondente : submeterArtigoController.getPossiveisAutoresCorrespondentes()) {
                    cmbAutorCorr.addItem(autorCorrespondente.getM_strNome());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tem que inserir um titulo e resumo do artigo!",
                        "Dados inv√°lidos!", JOptionPane.INFORMATION_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this,
                    "Tem que seleccionar um evento primeiro");
        }
    }//GEN-LAST:event_btnSubmeterActionPerformed

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        if (cmbAutorCorr.getSelectedIndex() != -1) {
            Autor autorCorrespondente = new Autor();
            autorCorrespondente.setM_Utilizador(submeterArtigoController.
                    obterAutor((String) cmbAutorCorr.getSelectedItem()));

            submeterArtigoController.setCorrespondente(autorCorrespondente);
            String nomeFicheiro = JOptionPane.showInputDialog(this, "Insira o ficheiro:",
                    "Submeter artigo", JOptionPane.INFORMATION_MESSAGE);
            submeterArtigoController.setFicheiro(nomeFicheiro);

            //Adiciona t√≥picos
            while (JOptionPane.showConfirmDialog(this, "Adicionar um t√≥pico?", "Submeter artigo",
                    JOptionPane.YES_NO_OPTION) == 0) {
                String tp = JOptionPane.showInputDialog(this, "Insira o t√≥pico:",
                        "Submeter artigo", JOptionPane.INFORMATION_MESSAGE);

                if (submeterArtigoController.
                        topicoExisteEvento(tp) == null) {
                    JOptionPane.showMessageDialog(this, "T√≥pico n√£o existe no evento!");
                } else {
                    submeterArtigoController.addTopico(submeterArtigoController.
                            topicoExisteEvento(tp));
                }
            }

        }

        if (submeterArtigoController.getTopicosArtigo() != null) {
            submeterArtigoController.setListaTopicosArtigo(
                    submeterArtigoController.getTopicosArtigo());
        }

        boolean sucesso = submeterArtigoController.registarSubmissao();
        if(!sucesso){
            JOptionPane.showMessageDialog(this,
                "Artigo n√£o submetido!",
                "Submeter artigo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this,
                "Artigo submetido com sucesso!",
                "Submeter artigo", JOptionPane.INFORMATION_MESSAGE);
        }

        dispose();

    }//GEN-LAST:event_btnSeleccionarActionPerformed

