    private void jButtonOkMecanismoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkMecanismoActionPerformed
        iMecanismo = cmbMecanismo.getSelectedIndex();
        listaDistribuicoes = new ArrayList<>();
        String revisoes = "";
        if (iMecanismo != -1) {
            distribuirRevisoesController.setMecanismoDistribuicao(iMecanismo);
            JOptionPane.showMessageDialog(this, "Revisões distribuidos com Sucesso", "Distribuir Revisões", JOptionPane.OK_OPTION);
            listaDistribuicoes = distribuirRevisoesController.getListaDistribuicoes();
            if (listaDistribuicoes != null) {
                for (Distribuicao distribuicao : listaDistribuicoes) {
                    revisoes = revisoes + distribuicao.getM_artigo().toString();
                    for (Revisor revisor : distribuicao.getM_listaRevisores()) {
                        revisoes = revisoes + ": " + revisor.getM_strNome() + ", ";
                    }
                    revisoes = revisoes + "\n";
                }
                JOptionPane.showMessageDialog(this, revisoes, "Distribuir Revisões!", JOptionPane.OK_OPTION);
            } else {
                JOptionPane.showMessageDialog(this, "Nao existem dados suficientes para efectuar a distribuicao!", "Erro!", JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Tem que seleccionar um mecanismo primeiro!", "Distribuir Revisões", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonOkMecanismoActionPerformed

