    private void jButtonOkEventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkEventoActionPerformed
        iEvento = cmbEvento.getSelectedIndex();
        if (iEvento != -1) {
            Evento e = new Evento();
            distribuirRevisoesController.setEvento(e);
            listaMecanimos = distribuirRevisoesController.getMecanismoDistribuicao();

            if (listaMecanimos.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Não existe mecanismo de distribuição", "Distribuir Revisões",
                        JOptionPane.ERROR_MESSAGE);
                dispose();
            } else {
                for (MecanismoDistribuicao Mecanismo : listaMecanimos) {
                    cmbMecanismo.addItem(Mecanismo.getNome());
                }
                jPanelEscolhaEvento.setVisible(false);
                jPanelEscolhaMecanismo.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Tem que seleccionar um evento primeiro", "Distribuir Revisões", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonOkEventoActionPerformed

